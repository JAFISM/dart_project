void main(){
  var shirtsize='L';

  switch(shirtsize){
    case 'S':{
      print('small size');
      break;
    }
    case 'M':{
      print('medium size');
      break;
    }
    case 'L':{
      print('large size');
      break;
    }
    case 'XL':{
      print('extra large size');
      break;
    }
    default:{
      print("none of these");
    }
  }
}